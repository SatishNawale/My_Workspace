# Addtion
def Add(No1,No2):
    Result = No1 + No2
    return Result

# Substraction
def Sub(No1, No2):
    Result = No1 - No2
    return Result

# Multiplication
def Mult(No1, No2):
    Result = No1 * No2
    return Result

# Division
def Div(No1, No2):
    Result = int(No1/No2)
    return Result
    